<!-- JavaScript for About Page -->
<script>
document.addEventListener('DOMContentLoaded', function() {
  // Animated counter for company stats
  const statNumbers = document.querySelectorAll('.stat-number');
  statNumbers.forEach(stat => {
    const target = parseInt(stat.getAttribute('data-count'));
    const suffix = stat.textContent.includes('%') ? '%' : '';
    let current = 0;
    const increment = target / 50;
    const timer = setInterval(() => {
      current += increment;
      if (current >= target) {
        current = target;
        clearInterval(timer);
      }
      stat.textContent = Math.floor(current) + suffix;
    }, 30);
  });
  
  // Timeline hover effects
  const timelineItems = document.querySelectorAll('.timeline-item');
  timelineItems.forEach(item => {
    item.addEventListener('mouseenter', function() {
      const year = this.querySelector('.timeline-year');
      year.style.transform = 'scale(1.2)';
      year.style.boxShadow = '0 0 20px rgba(240, 165, 0, 0.5)';
    });
    
    item.addEventListener('mouseleave', function() {
      const year = this.querySelector('.timeline-year');
      year.style.transform = 'scale(1)';
      year.style.boxShadow = 'none';
    });
  });
  
  // Leader card hover effects
  const leaderCards = document.querySelectorAll('.leader-card');
  leaderCards.forEach(card => {
    card.addEventListener('mouseenter', function() {
      const image = this.querySelector('.leader-image img');
      image.style.transform = 'scale(1.1)';
    });
    
    card.addEventListener('mouseleave', function() {
      const image = this.querySelector('.leader-image img');
      image.style.transform = 'scale(1)';
    });
  });
  
  // Value card hover effects
  const valueCards = document.querySelectorAll('.value-card');
  valueCards.forEach(card => {
    card.addEventListener('mouseenter', function() {
      const icon = this.querySelector('.value-icon');
      icon.style.transform = 'rotate(15deg) scale(1.1)';
    });
    
    card.addEventListener('mouseleave', function() {
      const icon = this.querySelector('.value-icon');
      icon.style.transform = 'rotate(0) scale(1)';
    });
  });
  
  // Award card hover effects
  const awardCards = document.querySelectorAll('.award-card');
  awardCards.forEach(card => {
    card.addEventListener('mouseenter', function() {
      const icon = this.querySelector('.award-icon');
      icon.style.transform = 'rotate(360deg)';
      icon.style.transition = 'transform 0.6s ease';
    });
    
    card.addEventListener('mouseleave', function() {
      const icon = this.querySelector('.award-icon');
      icon.style.transform = 'rotate(0)';
    });
  });
  
  // Initialize scroll animations
  const scrollAnimates = document.querySelectorAll('.scroll-animate');
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
      }
    });
  }, { threshold: 0.1 });
  
  scrollAnimates.forEach(el => observer.observe(el));
  
  // Parallax effect for hero
  const heroBg = document.querySelector('.about-hero .hero-background');
  if (heroBg) {
    window.addEventListener('scroll', function() {
      const scrolled = window.pageYOffset;
      const rate = scrolled * -0.5;
      heroBg.style.transform = `translateY(${rate}px)`;
    });
  }
});
</script>